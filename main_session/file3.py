### Type casting

name = "Jeeva"
age = 25
gpa = 8.5

print(type(name))
print(type(age))
print(type(gpa))

# Type casting - convert int to float
age = float(age)
print(type(age))
print(age)

# Type casting - convert float to string
age1 = str(age)
print(type(age1))
print(age1)

age = age + "ageTest"
print(age)








